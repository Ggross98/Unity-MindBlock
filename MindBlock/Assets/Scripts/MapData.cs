            }
        }

        map = m;

        startPos = StartPosition();
        goalPos = GoalPosition();
        
    }


    public Vector2Int StartPosition()
    {
        for (int i = 0; i < row; i++)
        {
            for (int j = 0; j < column; j++)
            {
                if (map[i, j] == Config.CELL_TYPE.PLAYER) return new Vector2Int(i, j);
            }
        }
        return new Vector2Int();

        /*
