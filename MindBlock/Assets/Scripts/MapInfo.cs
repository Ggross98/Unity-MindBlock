    public Vector2Int StartPosition()
    {
        for(int i = 0; i < map.Length; i++)
        {
            if(map[i] == CELL_PLAYER)
            {
                return new Vector2Int(i % 20, 9 - (i / 20));
            }
        }

        return new Vector2Int();
    }

    public Vector2Int GoalPosition()
    {
        for (int i = 0; i < map.Length; i++)
        {
            if (map[i] == CELL_GOAL)
            {
                return new Vector2Int(i % 20, 9 - (i / 20));
            }
        }

        return new Vector2Int();
    }


